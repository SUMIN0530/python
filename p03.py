# 연산자
'''
/ : 나눗셈 (실수 반환)
// : 나눗셈 (몫 연산 = 정수 반환)
% : 나머지
** : 거듭제곱
순서는 사칙연산과 동일 (): 가장 먼저
문자열 끼리의 +, * 가능
'''

# 복합 대입
'''
a += 5 -> a = a + 5
a /= 11 -> a = a / 11
a **= 200 -> a = a ** 200
a %= 3 -> a = a % 3
'''

# 입력 input() --- 항상 문자열 -> 숫자계산시 형 변환 필수
a = input()  # 기본형
name = input('이름을 입력하세요: ')  # 프롬프트 메시지 추가
print('안녕하세요.', name, '입니다.')

# input() 숫자 입력
a = int(input('점수를 입력하세요: '))  # int() 정수형
print('점수는', a, '점 입니다.')
print(f'점수는 {a}점 입니다.')  # 좀 더 깔끔해 보임 + 편리함

score = int(input("점수를 입력하세요: "))
print(f'점수는 : {score + 10}')  # int() 없으면 에러 / 숫자 형 변형이 안됐기때문

# split() : 문자열 구분자 / ()안에 구분자 지정 가능 -> ()을 문자열에서 찾아 기준으로 리스트를 뽑음
fruit = '사과,참외,수박'.split(',')  # 공백이 기본값
print(fruit)  # ['사과', '참외', '수박']

# 변수처럼 한번에 많은 값 입력하기
'''
input().split()을 사용하면:
1. 사용자가 공백으로 구분된 여러 값을 한 번에 입력
2. split()으로 각각 분리
3. 여러 변수에 동시에 할당 가능
'''
fruit1, fruit2, fruit3 = input('과일 이름을 입력하세요: ').split()  # 입력란 생성
print(fruit1, fruit2, fruit3, sep='-')  # 리스트 생성시 -로 구분
